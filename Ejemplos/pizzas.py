#imprime una lista de pizzas favoritas

pizzas = ['peperoni','chicken','beef','jam','pineapple','sausage']
for pizza in pizzas:
    print("I like "+pizza.title()+" pizza!")
print("I really love pizza")

#4.10 use Slice

print("The first three item in the list are: ")
print(pizzas[:3])

print("The three items from the middle of the list are: ")
print(pizzas[2:4])

print("The last three items in the list are: ")
print(pizzas[3:])






