#Ordenar listas con el metodo sort()

cars = ['bmw','audi','toyota','subaru']
cars.sort()
print(cars)

#Ordenar de forma inversa

cars.sort(reverse=True) #reverse = True argumento utilizado
print(cars)


#Ordenar temporalmente usando sorted()

cars = []
cars = ['bmw','audi','toyota','subaru']

print("Here is the original list: ")
print(cars)

print("\nHere is the sorted list: ")
print(sorted(cars))

print("\nHere is the original list again: ")
print(cars)

#Imprimir lista alreves  usando reverse()

print(cars)
cars.reverse()
print(cars)




